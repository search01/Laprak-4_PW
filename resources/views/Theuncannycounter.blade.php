    <span>Sinopsis The Uncanny Counter</span>
    <p>Sekelompok orang dikenal sebagai “Counter.” Tujuan mereka adalah untuk memburu roh jahat dan mereka masing-masing memiliki kemampuan khusus. Kelompok tersebut menyamar sebagai karyawan di restoran mie.
        So Moon (diperankan oleh Joe Byeong-Gyu) adalah seorang siswa sekolah menengah dan dia adalah anggota termuda dari “Counter.” Kemampuan khususnya adalah kekuatan fisiknya. Sebagai seorang anak, dia terlibat dalam kecelakaan mobil yang mencurigakan. Orang tuanya meninggal dalam kecelakaan itu dan dia menderita pincang di kaki kirinya.
        Ga Mo-Tak (diperankan oleh Yu Jun-Sang) adalah mantan petugas polisi. Dia mengalami kecelakaan 7 tahun yang lalu, yang menyebabkan dia kehilangan ingatan. Dia adalah orang yang benar.
        Do Ha-Na (diperankan oleh Kim Se-Jeong) dapat merasakan roh jahat dari jarak ratusan kilometer. Saat dia menyentuh seseorang, dia bisa membaca ingatan mereka.
        Chu Mae-Ok (diperankan oleh Yum Hye-Ran) adalah koki restoran mie. Dia adalah pembawa berita “Counter.”
        Choi Jang-Mool (diperankan oleh Ahn Suk-Hwan) adalah “Counter” pertama di Korea. Dia bertanggung jawab untuk mengatur pengeluaran dari grup.
