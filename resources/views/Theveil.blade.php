        Dia telah mendorong sindikat kejahatan terorganisir ke sudut ketika dia menghilang dari muka bumi, tetapi dia kembali setahun kemudian untuk mengguncang sindikat itu sekali lagi.
        Seo Soo Yeon adalah kepala tim 4 Pusat Informasi Kejahatan yang sangat baik dalam menjalankan tugasnya dan memecahkan kasus. 
        Dia memiliki kasih sayang yang besar untuk orang-orang, dan dia terutama peduli untuk rekan satu timnya.
